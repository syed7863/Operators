package syed;

public class ForLoop {

	public static void main(String[] args) {
		for(int i=1; i<=5;i++)
		{
			System.out.println("Hello");
		}
		// 1,2,3,4,5
		
		for(int s=46; s<=150;s++)
		{
			System.out.println(s);
		}
		//5,4,3,2,1
		
		for(int d=5; d>=1; d--)
		{
			System.out.println(d);
		}
		
		//2,4,6,8,10
		
		for(int r=2; r<=10; r+=2)
		{
			System.out.println(r);
		}

	}

}
