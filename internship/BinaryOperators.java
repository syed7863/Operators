package syed;

public class BinaryOperators {
	public static void main(String[] args) {
		int num1=50;
		int num3=10;
		int sum=num1+num3;
		System.out.println("Sum-" +sum);
		System.out.println("Sub-" +(num1-num3));
		System.out.println("Remain-" +(num1%num3));
		System.out.println("Mul-" +(num1*num3));
		
		
	}

}
